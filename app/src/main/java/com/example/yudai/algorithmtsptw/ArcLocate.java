package com.example.yudai.algorithmtsptw;

public class ArcLocate {
    private Locate mLocate1;
    private Locate mLocate2;

    public ArcLocate(Locate mLocate1, Locate mLocate2) {
        this.mLocate1 = mLocate1;
        this.mLocate2 = mLocate2;
    }

    public Locate getmLocate1() {
        return mLocate1;
    }

    public void setmLocate1(Locate mLocate1) {
        this.mLocate1 = mLocate1;
    }

    public Locate getmLocate2() {
        return mLocate2;
    }

    public void setmLocate2(Locate mLocate2) {
        this.mLocate2 = mLocate2;
    }
}
